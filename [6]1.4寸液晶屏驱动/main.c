//I2C init ----------------------------------------------------------------------
sbit I2CRST = P2^0; //定义I2C引脚
sbit SCL =P2^1;
sbit SDA = P2^2;

void I2C_SOFT_START() //软件模拟I2C 开始信号
{
	SDA = 1;
	delayus();
	SCL = 1;
	delayus();
	SDA = 0;
	delayus();
	SCL	=	0;
	SDA	= 1;
}

void I2C_SOFT_STOP() //软件模拟I2C 停止信号
{
	SDA = 0;
	delayus();
	SCL = 1;
	delayus();
	SDA = 1;
	delayus();
	SCL	=	0;
	SDA	= 1;
}


void I2C_SOFT_SEND(uchar S_SND)   //软件模拟I2C，写1字节数据
{
	uchar i,SOFT_ACK,SND;
	SOFT_ACK = 1;
while (SOFT_ACK)	
{
	SND = S_SND;
 for (i=8;i!=0;i--)
	{
		if(SND & 0X80)
			SDA = 1;
		else SDA = 0;
		SND <<=1;
		delayus();
//		delayus();
		SCL = 1;
		delayus();
//		delayus();
		SCL=0;
		delayus();
	}
		SDA =1;
		delayus();
		SCL = 1;
		if(~SDA)
		{
			delayus();  //貌似少了这个延时，有时候不能顺利初始化
			SOFT_ACK = 0; //判断是否重发
		}SCL=0;
}
}

